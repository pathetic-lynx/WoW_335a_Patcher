inline std::vector<uint8_t> read_bytes_at(const std::string& path,
                                          std::streampos offset, size_t n) {
  std::ifstream f(path, std::ios::binary);
  if (!f) return {};
  f.seekg(offset);
  if (!f) return {};
  std::vector<uint8_t> buf(n);
  f.read(reinterpret_cast<char*>(buf.data()), static_cast<std::streamsize>(n));
  buf.resize(static_cast<size_t>(f.gcount()));
  return buf;
}

inline PatchStatus check_byte_patch(const std::string& path,
                                    const BytePatch& bp) {
  size_t n = std::max(bp.original.size(), bp.replacement.size());
  auto actual = read_bytes_at(path, bp.offset, n);
  if (actual.size() >= bp.replacement.size() &&
      std::equal(bp.replacement.begin(), bp.replacement.end(), actual.begin()))
    return PatchStatus::Applied;
  if (actual.size() >= bp.original.size() &&
      std::equal(bp.original.begin(), bp.original.end(), actual.begin()))
    return PatchStatus::Unpatched;
  return PatchStatus::Mismatch;
}

inline PatchStatus check_patch(const std::string& path, const Patch& p) {
  if (p.changes.empty()) return PatchStatus::Mismatch;
  PatchStatus first = check_byte_patch(path, p.changes[0]);
  for (size_t i = 1; i < p.changes.size(); i++)
    if (check_byte_patch(path, p.changes[i]) != first)
      return PatchStatus::Mismatch;
  return first;
}
